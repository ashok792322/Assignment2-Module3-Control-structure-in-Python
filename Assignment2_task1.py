# Task1:Check if a Number is Even or Odd.
num = int(input("Enter a number: "))     # num is assigned value entered by user through input()
if num % 2 == 0:                         # if statement checking condition for even or odd, Give True or False as per user input received from line 1
    print(f"{num} is a even number")     # If block start , control enter if condition is true.
else:                                    # else statement for if conditional statement false
    print(f"{num} is a odd number")      # else block for if conditional statement False


    